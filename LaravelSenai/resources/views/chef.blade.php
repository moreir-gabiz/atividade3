    <h1>Receita de Banoffe Perfeita </h1>

    <p>Combinando a doçura da banana, a cremosidade do doce de leite e a leveza do chantilly, a <br>
        torta Banoffee é uma verdadeira explosão de sabor!Fácil de preparar e impossível de resistir, essa <br>
        sobremesa especial conquista corações desde a primeira colherada. Ideal para impressionar sem complicar!</p> <br>

    <h2>Ingredientes</h2>

    <ul>
        <li>200 g de bolacha maisena</li>
        <li>4 bananas maduras</li>
        <li>1 pote de nata</li>
        <li>Canela em pó a gosto</li>
        <li>100g de manteiga amolecida</li>
        <li>2 latas de leite condensado</li>
        <li>4 colheres (sopa) rasas de açúcar</li>
        <li>achocolatado em pó a gosto</li>
    </ul>

    <h2>Modo de Preparo </h2>
     <ol>
        <li>Faça uma farofa com a bolacha de maisena e manteiga.</li>
        <li>Espalhe somente no fundo de um refratário</li>
        <li>Coloque para assar em forno preaquecido a 200º C por 7 minutos.</li>
        <li>Fatie as bananas caturras no sentido comprido. </li>
        <li>Coloque os leites condensados em uma panela de pressão por 35 minutos. Deixe esfriar.</li>
        <li>Bata a nata e o açúcar na batedeira até virar um creme.</li>


        <li>Depois de frio, espalhe o leite condensado sobre a massa na forma.</li>
        <li>Cubra o leite condensado com a banana fatiada.</li>
        <li>Sobre a banana, espalhe o creme da nata.</li>
        <li>Alise bem e com uma peneira fina, polvilhe com chocolate em pó e um pouco de canela. </li>
        <li>Leve à geladeira por 3 horas.</li>

     </ol>
